package com.bopinjia.customer.bean;

public class DropRedBean {

	private String amount;
	private String red_type;
	private String red_detail;
	private String red_time;
	private String red_isuse;
	private String red_id;

	public String getRed_id() {
		return red_id;
	}

	public void setRed_id(String red_id) {
		this.red_id = red_id;
	}

	public String getAmount() {
		return amount;
	}

	public void setAmount(String amount) {
		this.amount = amount;
	}

	public String getRed_type() {
		return red_type;
	}

	public void setRed_type(String red_type) {
		this.red_type = red_type;
	}

	public String getRed_detail() {
		return red_detail;
	}

	public void setRed_detail(String red_detail) {
		this.red_detail = red_detail;
	}

	public String getRed_time() {
		return red_time;
	}

	public void setRed_time(String red_time) {
		this.red_time = red_time;
	}

	public String getRed_isuse() {
		return red_isuse;
	}

	public void setRed_isuse(String red_isuse) {
		this.red_isuse = red_isuse;
	}

}
